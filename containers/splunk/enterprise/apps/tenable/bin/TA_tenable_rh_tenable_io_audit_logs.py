class GetSessionKey(admin.MConfigHandler):
    def __init__(self):
        self.session_key = self.getSessionKey()

class TenableIOAuditLog(Validator):
    def __init__(self, *args, **kwargs):
        """

        :param validator: user-defined validating function
        """
        super(TenableIOAuditLog, self).__init__()

    def validate(self, value, data):
        try:
            acc_name = data.get("global_account")
            s_key = GetSessionKey().session_key
            _, content = rest.simpleRequest(
                "/servicesNS/nobody/TA-tenable/configs/conf-ta_tenable_account/{}".format(acc_name),
                sessionKey=s_key,
                getargs={"output_mode": "json"},
                raiseAllErrors=True)
            account_data = json.loads(content)["entry"]
            content = account_data[0]["content"]
            creds = get_passwords(account_data[0]["name"], "TA-tenable", s_key)
            access_key = creds.get("access_key", "")
            secret_key = creds.get("secret_key", "")
            proxies = get_proxy_settings(global_account_dict=content)
            tio = TenableIO(
                access_key=access_key,
                secret_key=secret_key,
                url="https://" + content.get("address"),
                proxies=proxies,
                vendor='Tenable',
                product='SplunkTA',
                build=get_app_version("TA-tenable")
            )
            if tio.session.details().get('permissions') < 64:
                self.put_msg("The Audit Log input requires that the user we connect with is a Tenable.io Administrator. Please update the account in Tenable.io and try again.")
                return False
            return True
        except Exception as e:
            self.put_msg("Error occured while creating the input. Error: {}".format(e))
            return False

class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """
    def delete_checkpoint(self, checkpoint_key):
        """Delete the checkpoint when user deletes input
        :return:
        """
        app_name = __file__.split(os.sep)[-3]
        checkpoint_collection = checkpointer.KVStoreCheckpointer(
            checkpoint_key, self.getSessionKey(), app_name
        )
        try:
            checkpoint_collection.delete(checkpoint_key)
        except Exception:
            raise

    def handleRemove(self, confInfo):
        """
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        try:
            input_name = self.callerArgs.id
            checkpoint_key = input_name
            self.delete_checkpoint(checkpoint_key)
            try:
                rest.simpleRequest(
                    "/servicesNS/nobody/TA-tenable/storage/collections/data/TA_tenable_checkpointer/{}".format(checkpoint_key),
                    sessionKey=self.getSessionKey(),
                    method="DELETE",
                    getargs={"output_mode": "json"},
                    raiseAllErrors=True,
                )
            except Exception:
                pass
        except Exception:
            raise
        finally:
            super(CustomConfigMigrationHandler, self).handleRemove(confInfo)

